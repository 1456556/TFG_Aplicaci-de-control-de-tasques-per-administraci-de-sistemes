    private void ActualitzarContrasenyaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualitzarContrasenyaActionPerformed
        // TODO add your handling code here:
        
        String password, confirmation_password;
        
        password = txt_password.getText().trim();
        confirmation_password = txt_confirmpassword.getText().trim();
        
        if (!password.equals("") && !confirmation_password.equals("")){
        
               if (password.equals(confirmation_password)){
               
                   try{
                   
                       Connection cn = Conexio.conectar();
                        PreparedStatement pst = cn.prepareStatement("update Usuaris set contrassenya=? where usuari = '" + user_update + "'"  );
                        
                        pst.setString(1,password);
                        
                        pst.executeUpdate();
                        cn.close();
                        txt_password.setBackground(Color.green);
                        txt_confirmpassword.setBackground(Color.green);                     
            
                        JOptionPane.showMessageDialog(null,"Restauraci√≥ Exitosa");
                        this.dispose();
                   
                   }catch(SQLException e){
                   
                       System.out.println("Error en resstaurar la contrasenya");
                   
                   
                   }
               
               
               }else{
                   txt_confirmpassword.setBackground(Color.red);
                   JOptionPane.showMessageDialog(null,"Les contrasenyes no concideixen");
               
               
               }        
        
        }else{
        
            txt_password.setBackground(Color.red);
            txt_confirmpassword.setBackground(Color.red);
            JOptionPane.showMessageDialog(null,"No s'admeten contrasenyes buides");
        
        
        
        }
    }//GEN-LAST:event_ActualitzarContrasenyaActionPerformed

